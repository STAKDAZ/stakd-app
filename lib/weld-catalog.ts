export type WeldCatalogRow = {
  shopSetup: string;
  weldType: string;
  weldProcess: string;
  thickness: string;
  ratePerHour: number;
  weightPerFt: number;
  costPerLb: number;
};

function mixedToDecimal(raw: string): number {
  const text = raw.trim();
  if (!text) return 0;
  if (text.includes('-')) {
    const [whole, frac] = text.split('-');
    return Number(whole || 0) + mixedToDecimal(frac);
  }
  if (text.includes(' ')) {
    return text.split(/\s+/).reduce((sum, part) => sum + mixedToDecimal(part), 0);
  }
  if (text.includes('/')) {
    const [num, den] = text.split('/').map(Number);
    return den ? num / den : 0;
  }
  return Number(text) || 0;
}

export function parseTeklaRate(raw: string): number {
  return mixedToDecimal(raw);
}

export function parseWeightPerFt(raw: string): number {
  return Number(raw.replace(/[^0-9.]/g, '')) || 0;
}

function row(shopSetup: string, weldType: string, weldProcess: string, thickness: string, rate: string, weight: string, cost = 0.9): WeldCatalogRow {
  return {
    shopSetup,
    weldType,
    weldProcess,
    thickness,
    ratePerHour: parseTeklaRate(rate),
    weightPerFt: parseWeightPerFt(weight),
    costPerLb: cost,
  };
}

